const mongoose = require('mongoose');
const { REGEXP_URL } = require('../constants');

const movieSchema = new mongoose.Schema(
  {
    country: {
      type: String,
      required: true,
    },
    director: {
      type: String,
      required: true,
    },
    duration: {
      type: Number,
      required: true,
    },
    year: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    image: {
      type: String,
      required: true,
    },
    trailerLink: {
      type: String,
      required: true,
    },
    thumbnail: {
      type: String,
      required: true,
    },
    owner: {
      type: mongoose.Schema.Types.ObjectId,
      ref: 'user',
      required: true,
    },
    movieId: {
      type: Number,
      required: true,
    },
    nameRU: {
      type: String,
      required: true,
    },
    nameEN: {
      type: String,
      required: true,
    },
  },
  {
    versionKey: false,
  },
);

movieSchema.path('image').validate((link) => REGEXP_URL.test(link), 'Invalid URL.');

movieSchema.path('trailerLink').validate((link) => REGEXP_URL.test(link), 'Invalid URL.');

movieSchema.path('thumbnail').validate((link) => REGEXP_URL.test(link), 'Invalid URL.');

module.exports = mongoose.model('movie', movieSchema);
